package JavaBasics.lesson2.lab2;

public class Calculator {
    public static void main(String[] args) {
    }
    // Сумма
    public static Double sum(Double a, Double b) { return a + b;}
    public static Integer sum(Integer a, Integer b) {return a + b;}
    public static Long sum(Long a, Long b) {return a + b;}

    //Вычитание
    public static Double substraction(Double a, Double b) {return a - b;}
    public static Integer substraction(Integer a, Integer b) {return a - b;}
    public static Long substraction(Long a, Long b) {return a - b;}

    //Умножение
    public static Double multiplicator(Double a, Double b) {return a * b;}
    public static Integer multiplicator(Integer a, Integer b) {return a * b;}
    public static Long multiplicator(Long a, Long b) {return a * b;}

    //Деление
    public static Double division(Double a, Double b) {return (double) a / b;}
    public static Integer division(Integer a, Integer b) {return a / b;}
    public static Long division(Long a, Long b) {return a / b;}
}
